import React, { useEffect, useState } from 'react';
import { Loader } from './Loader';
import { NewCommentForm } from './NewCommentForm';

import * as commentsApi from '../api/comments';
import { CommentData } from '../types/Comment';
import { useAppDispatch, useAppSelector } from '../app/hooks';
import { addComment, loadComments } from '../app/thunks/commentThunks';
import { deleteComment } from '../app/slices/commentsSlice';

export const PostDetails: React.FC = () => {
  const dispatch = useAppDispatch();
  const { selectedPost: post } = useAppSelector(state => state.posts);
  const {
    hasError,
    loaded,
    comments,
  } = useAppSelector(state => state.comments);
  const [visible, setVisible] = useState(false);
  const handleVisible = () => setVisible(true);
  const isNoComments = loaded && !hasError && comments.length === 0;

  useEffect(() => {
    setVisible(false);
    dispatch(loadComments(post?.id as number));
  }, [post?.id]);

  const handleAddComment = async ({ name, email, body }: CommentData) => {
    const trimmedName = name.trim();
    const trimmedEmail = email.trim();
    const trimmedBody = body.trim();

    if (trimmedName === '' || trimmedEmail === '' || trimmedBody === '') {
      return;
    }

    const data = {
      name: trimmedName,
      email: trimmedEmail,
      body: trimmedBody,
      postId: post?.id as number,
    };

    await dispatch(addComment(data));
  };

  const handleDeleteComment = (commentId: number) => () => {
    dispatch(deleteComment(commentId));
    commentsApi.deleteComment(commentId);
  };

  return (
    <div className="content" data-cy="PostDetails">
      <div className="block">
        <h2 data-cy="PostTitle">
          {`#${post?.id}: ${post?.title}`}
        </h2>

        <p data-cy="PostBody">
          {post?.body}
        </p>
      </div>

      <div className="block">
        {!loaded && (
          <Loader />
        )}

        {loaded && hasError && (
          <div className="notification is-danger" data-cy="CommentsError">
            Something went wrong
          </div>
        )}

        {isNoComments && (
          <p className="title is-4" data-cy="NoCommentsMessage">
            No comments yet
          </p>
        )}

        {loaded && !hasError && comments.length > 0 && (
          <>
            <p className="title is-4">Comments:</p>

            {comments.map(comment => (
              <article
                className="message is-small"
                key={comment.id}
                data-cy="Comment"
              >
                <div className="message-header">
                  <a href={`mailto:${comment.email}`} data-cy="CommentAuthor">
                    {comment.name}
                  </a>

                  <button
                    data-cy="CommentDelete"
                    type="button"
                    className="delete is-small"
                    aria-label="delete"
                    onClick={handleDeleteComment(comment.id)}
                  >
                    delete button
                  </button>
                </div>

                <div className="message-body" data-cy="CommentBody">
                  {comment.body}
                </div>
              </article>
            ))}
          </>
        )}

        {loaded && !hasError && !visible && (
          <button
            data-cy="WriteCommentButton"
            type="button"
            className="button is-link"
            onClick={handleVisible}
          >
            Write a comment
          </button>
        )}

        {loaded && !hasError && visible && (
          <NewCommentForm onSubmit={handleAddComment} />
        )}
      </div>
    </div>
  );
};
